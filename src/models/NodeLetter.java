package models;

public class NodeLetter {
	private char info;
	private int mark;

	public NodeLetter(char info) {
		this.info = info;
	}

	public char getInfo() {
		return info;
	}

	public void setInfo(char info) {
		this.info = info;
	}

	public int getMark() {
		return mark;
	}

	public void setMark(int mark) {
		this.mark = mark;
	}

}
